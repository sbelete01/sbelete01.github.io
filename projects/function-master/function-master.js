function hasWord(string, word) {
    let arrOfStr = string.split(" ");
    for (let i = 0; i <arrOfStr.length; i++){
        if(arrOfStr[i].toUpperCase() === word.toUpperCase()){
            return true;
        }
    }
    return false;
}

